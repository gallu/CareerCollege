<?php  // cal.php

$i = 10;
$j = 3;

// 足し算
$k = $i + $j;
echo $k;
echo "<br>\n";
// 引き算
$k = $i - $j;
echo $k;
echo "<br>\n";
// 掛け算
$k = $i * $j;
echo $k;
echo "<br>\n";
// 割り算
$k = $i / $j;
echo $k;
echo "<br>\n";

// 剰余
$k = $i % $j;
echo $k;
echo "<br>\n";

// べき乗
$k = $i ** $j; // pow()でもよい
echo $k;
echo "<br>\n";
