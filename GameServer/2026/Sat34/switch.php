<?php  // switch.php

$num = 200;

switch ($num) {
    case "2e2":
        echo "num is 2e2 <br>";
        break;
    case 100:
        echo "num is 100 <br>";
        break;
    case 200:
        echo "num is 200 <br>";
        break;
    default:
        echo "num is any <br>";
        break;
}

